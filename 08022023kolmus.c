int cross_sum(int num) {

    int sum = 0;

    int mod = 10;
    sum += num % mod;

    do {
        mod *= 10;
        sum += (num % mod) / (mod / 10);

    } while (num % mod != num);

    return sum;
}

int cross_sum_sum(int arr[], int arr_size) {

    int sum = 0;

    for (int i = 0; i < arr_size; i++) {
        sum += cross_sum(arr[i]);
    }

    return sum;
}

