int is_in_array(int *arr, int arr_size, int element) {
    
    for (int i = 0; i < arr_size; i++) {
        if (arr[i] == element)
            return 1;
    }

    return 0;
}

int remove_duplicates(int arr[], int arr_size, int *new_arr) {
    
    int new_arr_size = 0;
    
    for (int i = 0; i < arr_size; i++) {
	
	    if (!is_in_array(new_arr, new_arr_size, arr[i]))
            new_arr[new_arr_size++] = arr[i];

    }

    return new_arr_size;
}

